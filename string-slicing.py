a = "Hello World"
print(a)

# start from 3rd character
print(a[3::])

print(a[3:])

# start skipping and print every 3rd charcter
print(a[::3])

# Start from beginning to 3rd character
print(a[:3])

# start from 3rd, stop at 4th
print(a[3:5:])

# Print backward
print(a[::-1])

# Print first 3 characters
print(a[:3:])

# Print first 7 characters, skipping at 2
print(a[:7:2])
